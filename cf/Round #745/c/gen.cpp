#include <bits/stdc++.h>
 
using namespace std;
 
int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(0);
	int n = int(2e5);
	int m = n;
	cout << n << ' ' << m << '\n';
	for (int i = 0; i < n; ++i) {
	  cout << 400 << ' ' << 400 << '\n'; 
	}

	for (int i = 0; i < m; ++i) {
	  cout << 1 << ' ' << i + 1 << '\n';
	}
}